a, b = 10, 3
print(a + b)   # Addition (13)
print(a - b)   # Subtraction (7)
print(a * b)   # Multiplication (30)
print(a / b)   # Division (3.33)
print(a // b)  # Floor Division (3)
print(a % b)   # Modulus (1)
print(a ** b)  # Exponentiation (10^3 = 1000)

#Comparision Operator
print(a > b)   # True
print(a == b)  # False
print(a != b)  # True
